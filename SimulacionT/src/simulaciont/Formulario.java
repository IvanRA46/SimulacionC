    private void agregar() {
        if (txtMarca.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Debe ingresar el nombre de la marca", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (txtNombre.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Debe ingresar el nombre del medio", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (jtxtCapacidad.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Debe ingresar el costo de entrada de la playa", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (jtxtCilindraje.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Debe ingresar la capacidad m√°xima de la playa", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (!jcbSi.isSelected() && !jcbNo.isSelected()) {
            JOptionPane.showMessageDialog(null, "Debe seleccionar si la playa tiene sombrillas o no", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (jcbTipo.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(null, "Debe seleccionar si la playa tiene restaurante o no", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        String nombre = txtNombre.getText();
        String marca = txtMarca.getText();
        int cilindraje = Integer.parseInt(jtxtCilindraje.getText());
        int capacidad = Integer.parseInt(jtxtCapacidad.getText());
        boolean placas = false;
        if (jcbNo.isSelected()) {
            placas = false;
        }
        if (jcbSi.isSelected()) {
            placas = true;
        }

        String tipo = jcbTipo.getSelectedItem().toString();
        //String marca, String tipo, int cilindraje, String nombre, boolean placas, int capacidad
        cola.encolar(nombre,  placas, capacidad, marca, tipo, cilindraje);
    }
